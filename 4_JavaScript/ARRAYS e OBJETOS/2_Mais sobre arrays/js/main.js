//Mais sobre Arrays
//Podemos acessar um array pelo seu indice;
//Lembrando que o primeiro indice sempre é zero [0];


let arr = ['maçã', 'laranja', 'banana', 'limão'];

console.log(arr[0]); //será o primeiro elemento 'maçã' pois zero 0 é o primeiro indice;
console.log(arr[arr.length -1]); //o comprimento é 4, porém o elemento 4 tem o indice 3 aceso o 'limão';